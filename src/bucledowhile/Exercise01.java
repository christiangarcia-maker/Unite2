package bucledowhile;

public class Exercise01 {

	public static void main(String[] args) {
		
		// Numero inicializado en 1
		int numero = 1;
		
		// Imprime numero para luego sumar uno a la variable.
		do {
			System.out.println(numero);
			numero++;
		// Si numero es menor o igual que 20 el bucle sigue.
		} while (numero<=20);

	}

}
